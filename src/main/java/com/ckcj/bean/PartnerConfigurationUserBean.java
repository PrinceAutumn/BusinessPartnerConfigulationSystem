package com.ckcj.bean;

public class PartnerConfigurationUserBean {
    private String Partners;
    private String PartnersName;
    private String PartnersCoordinates;
    private String PartnersRegions;
    private String Operation;

    public String Partners() {

        return Partners;
    }

    public void Partners(String Partners) {

        this.Partners = Partners;
    }

    public String PartnersName() {

        return PartnersName;
    }

    public void PartnersName(String PartnersName) {

        this.PartnersName = PartnersName;
    }

    public String PartnersCoordinates() {

        return PartnersCoordinates;
    }

    public void PartnersCoordinates(String PartnersCoordinates) {

        this.PartnersCoordinates = PartnersCoordinates;
    }

    public String PartnersRegions() {

        return PartnersRegions;
    }

    public void PartnersRegions(String PartnersRegions) {

        this.PartnersRegions = PartnersRegions;
    }

    public String Operation() {

        return Operation;
    }

    public void Operation(String Operation) {

        this.Operation = Operation;
    }
}